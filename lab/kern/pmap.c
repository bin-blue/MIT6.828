// This simple physical memory allocator is used only while JOS is setting
// up its virtual memory system.  page_alloc() is the real allocator.  这个简单的物理内存分配器只在JOS设置其虚拟内存系统时使用。Page_alloc()是真正的分配器。
//
// If n>0, allocates enough pages of contiguous physical memory to hold 'n'
// bytes.  Doesn't initialize the memory.  Returns a kernel virtual address.  如果n>0，则分配足够的连续物理内存页以容纳'n'字节。不初始化内存。返回一个内核虚拟地址。
//
// If n==0, returns the address of the next free page without allocating
// anything.															如果n==0，返回下一个空闲页的地址，而不分配任何东西
//
// If we're out of memory, boot_alloc should panic.						如果我们没有内存，boot_alloc应该会恐慌。
// This function may ONLY be used during initialization,
// before the page_free_list list has been set up.						这个函数只能在初始化过程中使用，也就是在page_free_list列表建立之前。
static void *
boot_alloc(uint32_t n)
{
	static char *nextfree;	// virtual address of next byte of free memory  可用内存下一个字节的虚拟地址
	char *result;

	// Initialize nextfree if this is the first time.  如果是第一次，则初始化nextfree。
	// 'end' is a magic symbol automatically generated by the linker,  'end'是一个由链接器自动生成的magic symbol，
	// which points to the end of the kernel's bss segment:   它指向内核BSS段的末尾:
	// the first virtual address that the linker did *not* assign
	// to any kernel code or global variables.   链接器 *not* 分配给任何内核代码或全局变量的第一个虚拟地址。
	if (!nextfree) {
		extern char end[];
		nextfree = ROUNDUP((char *) end, PGSIZE);
	}

	// Allocate a chunk large enough to hold 'n' bytes, then update  分配一个足够大的chunk来容纳'n'字节，然后更新nextfree。
	// nextfree.  Make sure nextfree is kept aligned
	// to a multiple of PGSIZE.   确保nextfree保持与PGSIZE的倍数对齐。
	//
	// LAB 2: Your code here.
	result = nextfree;
	if(n>0){
		nextfree = ROUNDUP(nextfree+n,PGSIZE);
		if (((uint32_t)nextfree-KERNBASE)>(npages*PGSIZE)){
			panic("boot_alloc:out_of_memory.Requested %uK, available %uK.\n", (uint32_t) nextfree / 1024, npages * PGSIZE / 1024);
		}
	}
	return result;
}

// Set up a two-level page table:   设置一个两层的页表
//    kern_pgdir is its linear (virtual) address of the root  kern_pgdir是root的线性（虚拟）地址
//
// This function only sets up the kernel part of the address space   这个函数只设置地址空间的内核部分
// (ie. addresses >= UTOP).  The user part of the address space
// will be set up later.   稍后再设置地址空间的用户部分
//
// From UTOP to ULIM, the user is allowed to read but not write.
// Above ULIM the user cannot read or write.   UTOP到ULIM的部分只允许用户读，ULIM以上的部分不允许用户读写
void
mem_init(void)
{
	uint32_t cr0;
	size_t n;

	// Find out how much memory the machine has (npages & npages_basemem).
	i386_detect_memory();

	// Remove this line when you're ready to test this function.
	// panic("mem_init: This function is not finished\n");

	//////////////////////////////////////////////////////////////////////
	// create initial page directory. 	创建初始页目录
	kern_pgdir = (pde_t *) boot_alloc(PGSIZE);
	memset(kern_pgdir, 0, PGSIZE);

	//////////////////////////////////////////////////////////////////////
	// Recursively insert PD in itself as a page table, to form
	// a virtual page table at virtual address UVPT.		递归地将PD自身作为一个页表插入，在虚拟地址UVPT上形成一个虚拟页表。
	// (For now, you don't have understand the greater purpose of the
	// following line.)

	// Permissions: kernel R, user R
	kern_pgdir[PDX(UVPT)] = PADDR(kern_pgdir) | PTE_U | PTE_P;

	//////////////////////////////////////////////////////////////////////
	// Allocate an array of npages 'struct PageInfo's and store it in 'pages'. 分配一个npages 'struct PageInfo'的数组，并将其存储在'pages'中。
	// The kernel uses this array to keep track of physical pages: for
	// each physical page, there is a corresponding struct PageInfo in this		内核使用这个数组来跟踪物理页面:对于每个物理页面，在这个数组中都有一个对应的结构体PageInfo。
	// array.  'npages' is the number of physical pages in memory.  Use memset  	'npages'是内存中的物理页数。使用memset将每个struct PageInfo的所有字段初始化为0。
	// to initialize all fields of each struct PageInfo to 0.
	// Your code goes here:
	pages = (struct PageInfo *)boot_alloc(npages * sizeof(struct PageInfo));
	// memset(pages, 0, npages * sizeof(struct PageInfo));


	//////////////////////////////////////////////////////////////////////
	// Now that we've allocated the initial kernel data structures, we set  现在我们已经分配了初始内核数据结构，接下来设置空闲物理页列表。
	// up the list of free physical pages. Once we've done so, all further   一旦我们这样做了，所有进一步的内存管理将通过page_*函数进行。
	// memory management will go through the page_* functions. In
	// particular, we can now map memory using boot_map_region    特别是，我们现在可以使用boot_map_region或page_insert来映射内存
	// or page_insert
	page_init();

	check_page_free_list(1);
	check_page_alloc();
	check_page();

	//////////////////////////////////////////////////////////////////////
	// Now we set up virtual memory

	//////////////////////////////////////////////////////////////////////
	// Map 'pages' read-only by the user at linear address UPAGES 
	// Permissions:
	//    - the new image at UPAGES -- kernel R, user R
	//      (ie. perm = PTE_U | PTE_P)
	//    - pages itself -- kernel RW, user NONE
	// Your code goes here:
	boot_map_region(kern_pgdir, UPAGES, PTSIZE, PADDR(pages), PTE_U | PTE_P);
	//////////////////////////////////////////////////////////////////////
	// Use the physical memory that 'bootstack' refers to as the kernel  使用“bootstack”所指的内核堆栈的物理内存
	// stack.  The kernel stack grows down from virtual address KSTACKTOP. 内核堆栈从虚拟地址KSTACKTOP向下增长
	// We consider the entire range from [KSTACKTOP-PTSIZE, KSTACKTOP)
	// to be the kernel stack, but break this into two pieces:
	//     * [KSTACKTOP-KSTKSIZE, KSTACKTOP) -- backed by physical memory
	//     * [KSTACKTOP-PTSIZE, KSTACKTOP-KSTKSIZE) -- not backed; so if
	//       the kernel overflows its stack, it will fault rather than
	//       overwrite memory.  Known as a "guard page".
	//     Permissions: kernel RW, user NONE
	// Your code goes here:
	boot_map_region(kern_pgdir, KSTACKTOP - KSTKSIZE, KSTKSIZE, PADDR(bootstack), PTE_W | PTE_P);
	
	//////////////////////////////////////////////////////////////////////
	// Map all of physical memory at KERNBASE.
	// Ie.  the VA range [KERNBASE, 2^32) should map to
	//      the PA range [0, 2^32 - KERNBASE)
	// We might not have 2^32 - KERNBASE bytes of physical memory, but
	// we just set up the mapping anyway.
	// Permissions: kernel RW, user NONE
	// Your code goes here:
	boot_map_region(kern_pgdir, KERNBASE, 0xffffffff - KERNBASE, 0, PTE_W | PTE_P);
	// Check that the initial page directory has been set up correctly.
	check_kern_pgdir();

	// Switch from the minimal entry page directory to the full kern_pgdir
	// page table we just created.	Our instruction pointer should be
	// somewhere between KERNBASE and KERNBASE+4MB right now, which is
	// mapped the same way by both page tables.
	//
	// If the machine reboots at this point, you've probably set up your
	// kern_pgdir wrong.
	lcr3(PADDR(kern_pgdir));

	check_page_free_list(0);

	// entry.S set the really important flags in cr0 (including enabling
	// paging).  Here we configure the rest of the flags that we care about.
	cr0 = rcr0();
	cr0 |= CR0_PE|CR0_PG|CR0_AM|CR0_WP|CR0_NE|CR0_MP;
	cr0 &= ~(CR0_TS|CR0_EM);
	lcr0(cr0);

	// Some more checks, only possible after kern_pgdir is installed.
	check_page_installed_pgdir();
}

// --------------------------------------------------------------
// Tracking of physical pages.
// The 'pages' array has one 'struct PageInfo' entry per physical page.
// Pages are reference counted, and free pages are kept on a linked list.
// --------------------------------------------------------------

//
// Initialize page structure and memory free list.
// After this is done, NEVER use boot_alloc again.  ONLY use the page
// allocator functions below to allocate and deallocate physical
// memory via the page_free_list.
//
void
page_init(void)
{
	// The example code here marks all physical pages as free.  这里的示例代码将所有物理页面标记为free.
	// However this is not truly the case.  What memory is free?
	//  1) Mark physical page 0 as in use.   将物理页0标记为正在使用。
	//     This way we preserve the real-mode IDT and BIOS structures
	//     in case we ever need them.  (Currently we don't, but...)
	//  2) The rest of base memory, [PGSIZE, npages_basemem * PGSIZE)
	//     is free.		剩余的基本内存([PGSIZE, npages_basemm * PGSIZE)是free的
	//  3) Then comes the IO hole [IOPHYSMEM, EXTPHYSMEM), which must
	//     never be allocated.  然后是IO hole [IOPHYSMEM, EXTPHYSMEM]，它绝不能被分配
	//  4) Then extended memory [EXTPHYSMEM, ...).
	//     Some of it is in use, some is free. Where is the kernel
	//     in physical memory?  Which pages are already in use for
	//     page tables and other data structures?   有些在使用，有些是free的。内核在物理内存中的什么位置?哪些页面已经用于页表和其他数据结构?
	//
	// Change the code to reflect this.
	// NB: DO NOT actually touch the physical memory corresponding to
	// free pages! 
	size_t i; 								//注意:不要实际接触空闲页对应的物理内存!
	pages[0].pp_ref = 1;
    for (i = 1; i < npages_basemem; i++) {
        pages[i].pp_ref = 0;
        pages[i].pp_link = page_free_list;
        page_free_list = &pages[i];
		// cprintf("%u\n",i);
    }
    for (i = IOPHYSMEM / PGSIZE; i < EXTPHYSMEM / PGSIZE; i++) {
        pages[i].pp_ref = 1;
		// cprintf("%u\n",i);
    }
    for (i = EXTPHYSMEM / PGSIZE; i < npages; i++) {
        if (i < PADDR(boot_alloc(0)) / PGSIZE) {
            pages[i].pp_ref = 1;
			// cprintf("%u\n",i);
        } else {
            pages[i].pp_ref = 0;
            pages[i].pp_link = page_free_list;
            page_free_list = &pages[i];
			// cprintf("%u\n",i);
        }
    }
}

//
// Allocates a physical page.  If (alloc_flags & ALLOC_ZERO), fills the entire  如果(alloc_flags & ALLOC_ZERO)，则用'\0'字节填充整个返回的物理页。
// returned physical page with '\0' bytes.  Does NOT increment the reference
// count of the page - the caller must do these if necessary (either explicitly
// or via page_insert).
//
// Be sure to set the pp_link field of the allocated page to NULL so
// page_free can check for double-free bugs. 将已分配的pp_link字段设置为null。
//
// Returns NULL if out of free memory.
//
// Hint: use page2kva and memset
struct PageInfo *
page_alloc(int alloc_flags)
{	
	// Fill this function in
   struct PageInfo *result;

    if (page_free_list == NULL) {
        return NULL;
    }

    result = page_free_list;
    page_free_list = page_free_list->pp_link;
    result->pp_link = NULL;
    if (alloc_flags & ALLOC_ZERO) {
        memset(page2kva(result), 0, PGSIZE); // 接受一个物理地址并返回相应的虚拟地址 
    }
    return result;
}

//
// Return a page to the free list.
// (This function should only be called when pp->pp_ref reaches 0.)
//
void
page_free(struct PageInfo *pp)
{
	// Fill this function in
	// Hint: You may want to panic if pp->pp_ref is nonzero or
	// pp->pp_link is not NULL.
    if (pp->pp_ref != 0 || pp->pp_link != NULL) {
        panic("page_free: cannot free page");
    }
    pp->pp_link = page_free_list;
    page_free_list = pp;
	

}

//
// Decrement the reference count on a page,
// freeing it if there are no more refs.  递减页上的引用计数，如果没有更多引用就释放它
//
void
page_decref(struct PageInfo* pp)
{
	if (--pp->pp_ref == 0)
		page_free(pp);
}

// Given 'pgdir', a pointer to a page directory, pgdir_walk returns
// a pointer to the page table entry (PTE) for linear address 'va'.    给定'pgdir'，一个指向页面目录的指针，pgdir_walk返回一个指向线性地址'va'的页面表条目(PTE)的指针
// This requires walking the two-level page table structure.   这需要遍历两级页表结构
//
// The relevant page table page might not exist yet.   相关的页表页可能还不存在
// If this is true, and create == false, then pgdir_walk returns NULL.   如果这个为真，并且create == false，那么pgdir_walk返回NULL
// Otherwise, pgdir_walk allocates a new page table page with page_alloc.  否则，pgdir_walk使用page_alloc分配一个新的页表页
//    - If the allocation fails, pgdir_walk returns NULL.    如果分配失败，pgdir_walk返回NULL
//    - Otherwise, the new page's reference count is incremented,  否则，新页面的引用计数将增加
//	the page is cleared,   清除页面
//	and pgdir_walk returns a pointer into the new page table page.   pgdir_walk返回指向新页表页的指针
//
// Hint 1: you can turn a PageInfo * into the physical address of the
// page it refers to with page2pa() from kern/pmap.h.   你可以通过kern/pmap.h中的page2pa()将一个PageInfo *转换为它所引用的页面的物理地址
//
// Hint 2: the x86 MMU checks permission bits in both the page directory
// and the page table, so it's safe to leave permissions in the page
// directory more permissive than strictly necessary.   x86 MMU检查页目录和页表中的权限位，所以在页目录中保留比严格必要的权限是安全的
//
// Hint 3: look at inc/mmu.h for useful macros that manipulate page
// table and page directory entries.    查看inc/mmu.h以获得操作页表和页目录条目的有用宏
//
pte_t *
pgdir_walk(pde_t *pgdir, const void *va, int create)
{
	// Fill this function in
	pde_t *pde = &pgdir[PDX(va)];
    uint32_t *pt_addr;

    if (!(*pde & PTE_P)) {
        if (!create) {
            return NULL;
        }

        struct PageInfo *pp;

        if ((pp = page_alloc(ALLOC_ZERO)) == NULL) {  // allocate and clear the page
            return NULL;
        }
        pp->pp_ref++;
        *pde = page2pa(pp) | PTE_P | PTE_W | PTE_U;
    }
    pt_addr = KADDR(PTE_ADDR(*pde));
    return (pte_t *)(pt_addr + PTX(va));
}

//
// Map [va, va+size) of virtual address space to physical [pa, pa+size)
// in the page table rooted at pgdir.  Size is a multiple of PGSIZE, and
// va and pa are both page-aligned. 将虚拟地址空间的[va, va+size)映射到根在pgdir的页表中的物理[pa, pa+size)。Size是PGSIZE的倍数，并且va和pa都是页面对齐的。
// Use permission bits perm|PTE_P for the entries. 对表项使用perm|PTE_P权限位
//
// This function is only intended to set up the ``static'' mappings
// above UTOP. As such, it should *not* change the pp_ref field on the
// mapped pages. 这个函数只用于设置UTOP上面的“静态”映射。因此，它应该*不*更改映射页面上的pp_ref字段
//
// Hint: the TA solution uses pgdir_walk  提示:TA解决方案使用pgdir walk
static void
boot_map_region(pde_t *pgdir, uintptr_t va, size_t size, physaddr_t pa, int perm)
{
	// Fill this function in
	size_t i;
    for (i = 0; i < size / PGSIZE; i++) {
        pte_t *pte;

        if ((pte = pgdir_walk(pgdir, (void *)va, 1)) == NULL) {
            panic("boot_map_region: allocation failure");
        }
        *pte = pa | perm | PTE_P;
        va += PGSIZE;
        pa += PGSIZE;
    }

}

//
// Map the physical page 'pp' at virtual address 'va'. 映射物理页'pp'在虚拟地址'va'
// The permissions (the low 12 bits) of the page table entry
// should be set to 'perm|PTE_P'.  页表条目的权限(低12位)应该设置为'perm|PTE_P'
//
// Requirements
//   - If there is already a page mapped at 'va', it should be page_remove()d.如果已经有一个页面映射到'va'，它应该是page_remove()
//   - If necessary, on demand, a page table should be allocated and inserted
//     into 'pgdir'.   如果有必要，根据需要，应该分配页表并插入到'pgdir'中
//   - pp->pp_ref should be incremented if the insertion succeeds.  如果插入成功，Pp ->pp_ref应该递增
//   - The TLB must be invalidated if a page was formerly present at 'va'. 如果一个页面之前在'va'出现，TLB必须失效
//
// Corner-case hint: Make sure to consider what happens when the same
// pp is re-inserted at the same virtual address in the same pgdir.  请务必考虑在相同pgdir中的相同虚拟地址上重新插入相同的pp时会发生什么情况
// However, try not to distinguish this case in your code, as this
// frequently leads to subtle bugs; there's an elegant way to handle
// everything in one code path.
//  但是，不要在代码中区分这种情况，因为这经常会导致微妙的错误;有一种优雅的方法可以在一个代码路径中处理所有内容
// RETURNS:
//   0 on success
//   -E_NO_MEM, if page table couldn't be allocated  E_NO_MEM,如果不能分配页表
//
// Hint: The TA solution is implemented using pgdir_walk, page_remove,
// and page2pa.
//
int
page_insert(pde_t *pgdir, struct PageInfo *pp, void *va, int perm)
{
	// Fill this function in
    pte_t *pte;

    if ((pte = pgdir_walk(pgdir, va, 1)) == NULL) {
        return -E_NO_MEM;
    }
    pp->pp_ref++;
    if (*pte & PTE_P) {
        page_remove(pgdir, va);
    }
    *pte = page2pa(pp) | perm | PTE_P;
    return 0;
}

//
// Return the page mapped at virtual address 'va'.  返回映射到虚拟地址'va'的页面
// If pte_store is not zero, then we store in it the address  如果pte_store不为零，则将该页的pte地址存储在其中
// of the pte for this page.  This is used by page_remove and
// can be used to verify page permissions for syscall arguments,
// but should not be used by most callers.  这由page_remove使用，并可用于验证系统调用参数的页面权限，但大多数调用者不应使用
//
// Return NULL if there is no page mapped at va.  如果没有页面映射到va，则返回NULL
//
// Hint: the TA solution uses pgdir_walk and pa2page.  
//
struct PageInfo *
page_lookup(pde_t *pgdir, void *va, pte_t **pte_store)
{	
	// Fill this function in
    pte_t *pte;

    if ((pte = pgdir_walk(pgdir, va, 0)) == NULL || (*pte & PTE_P) == 0) {
        return NULL;
    }
    if (pte_store) {
        *pte_store = pte;
    }
    return pa2page(PTE_ADDR(*pte));
}

//
// Unmaps the physical page at virtual address 'va'. 解除物理页在虚拟地址'va'的映射
// If there is no physical page at that address, silently does nothing. 如果该地址上没有物理页面，则静默不做任何操作
//
// Details: 
//   - The ref count on the physical page should decrement. 物理页上的ref计数应该减少
//   - The physical page should be freed if the refcount reaches 0. 如果引用计数达到0，则应该释放物理页
//   - The pg table entry corresponding to 'va' should be set to 0. 对应于'va'的pg表项应该设置为0
//     (if such a PTE exists)
//   - The TLB must be invalidated if you remove an entry from
//     the page table.  如果从页表中删除一个条目，则TLB必须失效
//
// Hint: The TA solution is implemented using page_lookup,
// 	tlb_invalidate, and page_decref.
//
void
page_remove(pde_t *pgdir, void *va)
{
	// Fill this function in
	struct PageInfo *pp;
    pte_t *pte;

    if ((pp = page_lookup(pgdir, va, &pte)) == NULL) {
        return;
    }
    page_decref(pp);
    *pte = 0;
    tlb_invalidate(pgdir, va);
	
}

//
// Invalidate a TLB entry, but only if the page tables being
// edited are the ones currently in use by the processor.  使TLB项无效，但仅当正在编辑的页表是处理器当前正在使用的页表时
// 
void
tlb_invalidate(pde_t *pgdir, void *va)
{
	// Flush the entry only if we're modifying the current address space. 只有在修改当前地址空间时才刷新条目
	// For now, there is only one address space, so always invalidate. 现在，只有一个地址空间，所以总是无效
	invlpg(va);
}


